import curses
import time

def draw_text(stdscr, text, y, x):
    for i, line in enumerate(text):
        stdscr.addstr(y + i, x, line)

def runtime(num):
    curses.wrapper(main, num)

def main(stdscr, num):
    stdscr.clear()
    curses.curs_set(0)

    passman_text = [
        "%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%",
        "%%*------------*%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%#-------=%%--------%%%%%%%%%%%%%%%%%%%%%%%%%%%%%",
        "%%%%----+%%-----%%%%##**##%%%%%%%#*#%##%%%%%#*#%##%%%+-------+=--=----%%%%%##**##%%%%%%####%%###%%%%",
        "%%%%------------%*---*%+----%%#---*%=---%%---%%---+%%+--+*------+#----%%#---+%*----*%#------------%%",
        "%%%%-----++++*%%%%*=--------*%%--------*%%=-------#%%+--+%=-----%#----%%%#=--------+%%%----*%+----%%",
        "%%#-------%%%%%%%+---=%%----=#%%%%%*----*%%%%%*----%#----*%----%%------%*----%%-----*%+-----#+----+#",
        "%%*-------%%%%%%%%=-----------%--------#%%--------*%#----*%#--*%%------%%+-----------#------++-----#",
        "%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%",
        "%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%"
    ]

    goodbye_text = [
        "%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%",
        "%%%%=-----*---%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%-----=%%=-----------#%%%%%%%%%%%%%%%%%%%%%%%%%%%%%",
        "%%------------%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%-----=%%=-------------%%%%%%%%%%%%%%%%%%%%%%%%%%%%",
        "%-----*%%%#---%%%%#------+%%%%%%+------*%%%%%*---------=%%%=----%%#-----%+-----#+---+%%%=-----+%%%%%",
        "%-----%%%*****%%*----------=%%+----------*%%-----------=%%%=----------*%%=-----#=---=%+----%=---*%%%",
        "%-----#%#-----#%-----%%*----%#----*%%+----%%----*%%----=%%%=----#%*-----%%+----%+--*%%-----=-----%%%",
        "%*-----=#=----%%=----%%-----%%-----%#----=%%----=%#-----*%+-----==------*%%*------*%%%----+%%+#%%%%%",
        "%%%=--------*%%%%#--------+%%%%+--------*%%%#------------%=------------%%%%%+----+%%%%%+--------%%%%",
        "%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%#---=%%%%%%%%%%%%%%%%%%%",
        "%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%-----%%%%%%%%%%%%%%%%%%%%",
        "%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%"
    ]

    sec_text = [
        "                     .:::::.                   ",
        "                    .:=*-:.                  ",
        "                   .:=**+-:.                 ",
        "           ..::.   .:=***+:::..              ",
        "         .::::..    .:=*****-::..            ",
        "       ..-**=:..    .:-**@@%**=::.           ",
        "       .:=**+-:.   .::**%@@@%**-:..          ",
        "  ...  .:=***+::::::-**%@@@@@**=:..   .::.   ",
        "..::.   .-**@%********@@@@@@@**-..    .:::.. ",
        ".:-:.   .:-+%@@@@@@@@@@@@@@@%*=:.     .:-*-.. ",
        ":+=:.   .:-*#@@@@@-     :@@@%*=:..    .:=*+:. ",
        "-+*=:::::=**#@@:          .#@**-:::.::-+***:. ",
        ":=********#@@=              -@#***********+:. ",
        ".:-***@@@@@@@:  :        .: .@@@@@*******=:.. ",
        " ..:-**#@@@@#. .@.      .#+. %@@@@#****-::.  ",
        "  ..::=*#@@@# .#@#      :@#. *@@@***=-:.     ",
        "    .:-**@@@* .@@@.    .@@#. +@@***:.        ",
        "     :-**%@@+ .@@@=    +@@%. =@@**=:.        ",
        "    ..-**%@@+  .+@@.   %@=.. +@@**-.         ",
        "     .:**%@@@+.             .%@%**-.         ",
        "     .:+*%@@@@@.          :@@@@**+-.         ",
        "     .:=**#@@@#.  ..  .-  :@@@***=:.         ",
        "     .::-+***#%-..*-  .+.=%#***+-:..         ",
        "       ...:-+*****##**#****=-::..            ",
        "            ..::=******=:::..                ",
        "                ..:::::..          "
    ]

    art = {
        0: passman_text,
        1: goodbye_text,
        2: sec_text,
    }

    max_y, max_x = stdscr.getmaxyx()
    start_y = max_y // 2 - len(art[num]) // 2
    start_x = max_x // 2 - len(art[num][0]) // 2

    num_bounces = 4  # Number of bounces before stopping
    bounce_speed = 0.1  # Speed of each bounce (in seconds)

    for _ in range(num_bounces):
        draw_text(stdscr, art[num], start_y, start_x)
        stdscr.refresh()
        time.sleep(bounce_speed)

        for i in range(len(art[num])):
            stdscr.addstr(start_y + i, start_x, " " * len(art[num][i]))
        stdscr.refresh()
        time.sleep(bounce_speed / 2)

    draw_text(stdscr, art[num], start_y, start_x)
    stdscr.refresh()
    time.sleep(1)